package Hopeless2D;

public class Vector2
{
    public int x;
    public int y;

    public int x()
    {
        return x;
    }

    public int y()
    {
        return y;
    }

    public Vector2()
    {
        x=0;
        y=0;
    }

    public Vector2(int X, int Y)
    {
        x=X;
        y=Y;
    }
}